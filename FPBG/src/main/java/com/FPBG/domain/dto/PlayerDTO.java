package com.FPBG.domain.dto;

public class PlayerDTO {

	private String rating;
	private String win_Ratio;
	private String top_10_Ratio;
	private String kill_Death_Ratio;
	private String kills;
	private String wins;
	private String damage_Per_Game;
	private String round_Played;

	public String getRating() {
		return rating;
	}
	public void setRating(String rating) {
		this.rating = rating;
	}
	public String getWin_Ratio() {
		return win_Ratio;
	}
	public void setWin_Ratio(String win_Ratio) {
		this.win_Ratio = win_Ratio;
	}
	public String getTop_10_Ratio() {
		return top_10_Ratio;
	}
	public void setTop_10_Ratio(String top_10_Ratio) {
		this.top_10_Ratio = top_10_Ratio;
	}
	public String getKill_Death_Ratio() {
		return kill_Death_Ratio;
	}
	public void setKill_Death_Ratio(String kill_Death_Ratio) {
		this.kill_Death_Ratio = kill_Death_Ratio;
	}
	public String getKills() {
		return kills;
	}
	public void setKills(String kills) {
		this.kills = kills;
	}
	public String getWins() {
		return wins;
	}
	public void setWins(String wins) {
		this.wins = wins;
	}
	public String getDamage_Per_Game() {
		return damage_Per_Game;
	}
	public void setDamage_Per_Game(String damage_Per_Game) {
		this.damage_Per_Game = damage_Per_Game;
	}
	public String getRound_Played() {
		return round_Played;
	}
	public void setRound_Played(String round_Played) {
		this.round_Played = round_Played;
	}
	
}
